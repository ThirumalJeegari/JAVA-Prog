class Boolean 
{
	public static void main(String args[])
	{
		boolean a,b;
		a=true;
		b=false;
		System.out.println(" a & b = "+(a&b));
		System.out.println(" a | b = "+(a|b));
		System.out.println(" a & a = "+(a&a));
		System.out.println(" b | b = "+(b|b));
		System.out.println(" !a = "+(!a));
		System.out.println(" !b = "+(!b));
	}
}