class Switch2
{
	public static void main(String args[])
	{
		int a = 4;

		switch (a)
		{
			case 1:
				System.out.println("UPPALAPATI");
				break;

			case 2:
				System.out.println("VENKATA");
				break;

			case 3:
				System.out.println("SURYANARAYANA");
				break;

			case 4:
				System.out.println("PRABHAS");
				break;

			case 5:
				System.out.println("RAJU");
				break;



		}
		
	}
}